package Inheritance;

public class InheritanceClass {
    public final String ad;
    public final String soyad;



    public InheritanceClass(String ad, String soyad) {
        this.ad = ad;
        this.soyad = soyad;
    }

    public String getAd() {
        return ad;
    }

    public String getSoyad() {
        return soyad;
    }

}
